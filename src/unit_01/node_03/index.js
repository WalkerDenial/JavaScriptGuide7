// 双斜杠后面的这些文字都是注释
// 一定要认证阅读注释：注释是对 JavaScript 代码的解释

// 变量是一个达标值的名字
// 变量要使用 let 关键字声明
let x; // 声明一个名叫 x 的变量

// 可以使用一个等号为变量赋值
x = 0; // 现在变量 x 的值就是 0
x; // => 0：变量求职的结果就是它的值

// JavaScript 支持几种不同的值
x = 1; // 数值
x = 0.01; // 数值可以是整数或实数
x = "hello world"; // 文本字符串包含在引号中
x = 'JavaScript'; // 单引号也用于界定字符串
x = true; // 布尔值
x = false; // 另一个布尔值
x = null; // null 是一个特殊值，意识是『没有值』
x = undefined; // undefined 也是一个特殊值，与 null 类似